package ex_08_Switch;

public class Lab040_IQ {

    public static void main(String[] args) {

        char code = 'C';
        switch (code){
            default:
                System.out.println("Hellooooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
    }}
}
