package ex_Task;

public class Task005_Increoperator {
    public static void main(String[] args) {
            int a = 10;
            System.out.println(++a + a++ + a++);
            System.out.println(a);

         //   Answer = 34 , 13

            /*
    5   |   a=10  | a=10
    6   |   a=11+11+12   |34
    7  |  a=13   |13
*/
}
    }


