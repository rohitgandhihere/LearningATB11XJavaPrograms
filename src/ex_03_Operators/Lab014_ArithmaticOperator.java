package ex_03_Operators;

public class Lab014_ArithmaticOperator {
    public static void main(String[] args) {


        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder

        int a = 20;
        int b = 3;
        float c = 3.0f;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a / c); // 6.666665
        System.out.println(a % b);
        System.out.println("a+b");
        System.out.println(a + b);

    }
}
